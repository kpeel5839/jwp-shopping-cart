CREATE TABLE Product (
     id BIGINT NOT NULL AUTO_INCREMENT,
     name VARCHAR(100) NOT NULL,
     price INT NOT NULL,
     image_url LONGTEXT NOT NULL,
     PRIMARY KEY (id)
);

CREATE TABLE Member (
    id BIGINT AUTO_INCREMENT,
    email VARCHAR(30) UNIQUE NOT NULL,
    password VARCHAR(20) NOT NULL,
    PRIMARY KEY (id)
)
